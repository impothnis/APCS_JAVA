package apcs;

public class VariableDeclaration {

	public static void main(String[] args) {

		// int type variable stores an integer number 
		int integerVariable = 7;
		System.out.println(integerVariable);

		// double type variable stores decimal number 
		double decimalVariable = 3.14;
		System.out.println(decimalVariable);

		//boolean type variable can save either true or false, nothing else boolean
		boolean booleanVariable = true; 
		System.out.println(booleanVariable);

		// String type variable saves text literals 
		String textLiteralVariable =
		"Hello World!"; System.out.println(textLiteralVariable);





	}

}
